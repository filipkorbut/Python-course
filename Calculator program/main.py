# Calculator

operation = input("Enter your choice (+, -, *, /): ")
number1 = float(input("Enter your first number: "))
number2 = float(input("Enter your second number: "))

if operation == "+":
    print(number1 + number2)
elif operation == "-":
    print(number1 - number2)
elif operation == "*":
    print(number1 * number2)
elif operation == "/":
    print(number1 / number2)



